def _glyph(ch: str) -> list[str]:
    u = ch.upper() if ch.isalpha() else ch
    if ch in _GLYPHS:
        return _GLYPHS[ch]
    if u in _GLYPHS:
        return _GLYPHS[u]
    return _GLYPHS["?"] if "?" in _GLYPHS else _GLYPHS[" "]


class BitmapFont:
    """Drop-in-ish replacement for pygame.font.Font."""

    def __init__(self, pixel_height: int) -> None:
        self.pixel_height = max(8, pixel_height)
        self.scale = max(1, self.pixel_height // 7)
        self.glyph_w = 6 * self.scale
        self.glyph_h = 7 * self.scale
        self.line_h = self.glyph_h + 2

    def render(self, text: str, antialias: bool, color: tuple[int, int, int]) -> pygame.Surface:
        text = text.upper() if self.pixel_height >= 28 else text
        chars = [c if c in _GLYPHS or c.upper() in _GLYPHS else "?" for c in text]
        if not chars:
            return pygame.Surface((1, self.line_h), pygame.SRCALPHA)
        w = len(chars) * self.glyph_w
        surf = pygame.Surface((w, self.line_h), pygame.SRCALPHA)
        x = 0
        for ch in chars:
            rows = _glyph(ch if ch in _GLYPHS else ch.lower() if ch.lower() in _GLYPHS else "?")
            for row_i, row in enumerate(rows):
                for col_i, bit in enumerate(row):
                    if bit == "1":
                        pygame.draw.rect(
                            surf,
                            color,
                            (
                                x + col_i * self.scale,
                                row_i * self.scale,
                                self.scale,
                                self.scale,
                            ),
                        )
            x += self.glyph_w
        return surf

    def size(self, text: str) -> tuple[int, int]:
        s = self.render(text, True, (255, 255, 255))
        return s.get_width(), s.get_height()


def _bitmap_fonts() -> dict:
    return {
        "title": BitmapFont(48),
        "large": BitmapFont(28),
        "med": BitmapFont(20),
        "small": BitmapFont(16),
        "_bitmap": True,
    }


def load_fonts_safe() -> dict:
    """Try pygame fonts; fall back to bitmap fonts (never touch pygame.font on 3.14)."""
    if sys.version_info >= (3, 14):
        return _bitmap_fonts()
    try:
        from pygame.font import Font, SysFont

        if not pygame.font.get_init():
            pygame.font.init()

        def _font(size: int, bold: bool = False) -> Font:
            try:
                return SysFont("arial,helvetica,sans-serif", size, bold=bold)
            except Exception:
                return Font(None, size)

        return {
            "title": _font(48, bold=True),
            "large": _font(28, bold=True),
            "med": _font(20),
            "small": _font(16),
            "_bitmap": False,
        }
    except Exception:
        return _bitmap_fonts()

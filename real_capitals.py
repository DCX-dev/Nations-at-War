def capital_lonlat_for_name(name: str) -> tuple[float, float] | None:
    if name in CAPITALS:
        return CAPITALS[name]
    if name in _ALIASES:
        return CAPITALS.get(_ALIASES[name])
    low = name.lower()
    for key, val in CAPITALS.items():
        if key.lower() == low:
            return val
    return None


def lonlat_to_grid(
    lon: float, lat: float, width: int, height: int
) -> tuple[int, int]:
    x = int((lon + 180.0) * (width / 360.0))
    y = int((90.0 - lat) * (height / 180.0))
    x = max(0, min(width - 1, x))
    y = max(0, min(height - 1, y))
    return x, y
